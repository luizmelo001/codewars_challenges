def decode_morse(morse_code):
    morse_code = morse_code.strip().split("   ")
    result = []
    
    for word in morse_code:
        letters = word.split(" ")
        decoded = "".join(MORSE_CODE[letter] for letter in letters)
        result.append(decoded)
        
    return " ".join(result)
